# 比較演算子
# a == b  a と b は等しい
x = 5
if x == 5 :
    print("xは5です")


a = 5
b = 3
# a < b   a は b より小さい
if a < b :
    print("a は b より小さい")

# a > b   a は b より大きい
if a > b :
    print("a は b より大きい")

# a != b  a と b は等しくない
if a != b :
    print("a と b は等しくない")

# a <= b  a は b 以下
if a <= b :
    print("a は b 以下")

# a >= b  a は b 以上
if a >= b :
    print("a は b 以上")
