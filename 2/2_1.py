# 四則演算

total = 120 + 3
print(total)

num1 = 120
num2 = 3

# 足し算
total = num1 + num2
print(total)

# 引き算
total = num1 - num2
print(total)

# 掛け算
total = num1 * num2
print(total)

# 割り算
total = num1 / num2
print(total)
print(type(total))

# 割り算（小数点以下切り捨て）
total = num1 // num2
print(total)
print(10 // 3)

# 剰余
total = num1 % num2
print(total)
print(7 % 4)

# べき乗
total = num1 ** num2
print(total)

# 計算の優先順位
total = num1 * ((2 - 3) * num2)
print(total)

# total = total + 3
total += 3
print(total)

# total = total - 3
total -= 3
